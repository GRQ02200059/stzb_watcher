with open('battle_sim/data.py', encoding='utf-8') as f:
    src = f.read()

count = 0
for hid, sid in fixes.items():
    # 匹配 'id':100030,...,'skill':0,
    old = f"'id':{hid},"
    # 找到该行，替换 'skill':0 为 'skill':sid
    # 用正则定位 id后的skill字段
    pattern = rf"('id':{hid},[^{{}}]*?'skill'):0"
    new = rf"\g<1>:{sid}"
    new_src, n = re.subn(pattern, new, src)
    if n:
        src = new_src
        count += n
        print(f'OK {hid} -> {sid}')
    else:
        print(f'MISS {hid}')

with open('battle_sim/data.py', 'w', encoding='utf-8') as f:
    f.write(src)

print(f'\n修复完成: {count} 处')

